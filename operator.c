// Oparetor

#include<stdio.h>
int main(){
	int a , b ;
	int add , sub , multi , div , rem ;
	scanf("%d %d",&a,&b);
	// Arithmatic Operator
	add = a + b ;
	sub = a - b;
	multi = a * b ;
	div = a / b;
	rem = a % b ;
	
	printf("Add = %d \n ", add);
	printf("Sub = %d \n ", sub);
	printf("Multi = %d \n ", multi);
	printf("div = %d \n ", div);
	printf("Reminder = %d \n ", rem);
	
}
